package guopuran.bwie.com.guopuran20181203;

public class Bean {
    private String tag;
    private String name;

    public Bean() {
    }

    public Bean(String tag, String name) {
        this.tag = tag;
        this.name = name;
    }

    public String getTag() {
        return tag;
    }

    public String getName() {
        return name;
    }
}
